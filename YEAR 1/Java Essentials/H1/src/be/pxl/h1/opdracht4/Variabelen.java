package be.pxl.h1.opdracht4;

public class Variabelen {
    public static void main(String[] args) {
        int i;
        int n = 3;
        double d;
        i = n + 4;
        System.out.println(i);
        i = n * 4;
        System.out.println(i);
        i = 7 + 2 * 3;
        System.out.println(i);
        n = n * (n + 2) * 4;
        System.out.println(n);
        d = 3.5 / 2;
        System.out.println(d);
        n = 7 / 4;
        System.out.println(n);
    }
}
